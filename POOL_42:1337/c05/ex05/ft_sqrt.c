int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	while (a <= nb && nb <= 2147483647)
	{
		if (nb == a * a)
			return (a);
		if (a > 46340)
			return (0);
		a++;
	}
	return (0);
}
