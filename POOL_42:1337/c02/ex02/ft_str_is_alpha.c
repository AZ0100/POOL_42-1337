int	ft_str_is_alpha(char *str)
{
	int	z;

	z = 0;
	while (str[z])
	{
		if ((str[z] >= 'A' && str[z] <= 'Z')
			|| (str[z] >= 'a' && str[z] <= 'z'))
		z++;
		else
			return (0);
	}
	return (1);
}
