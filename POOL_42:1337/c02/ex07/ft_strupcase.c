char	*ft_strupcase(char *str)
{
	int	z;

	z = 0;
	while (str[z])
	{
		if (str[z] >= 'a' && str[z] <= 'z')
		{
			str[z] -= 32;
		}
		z++;
	}
	return (str);
}
